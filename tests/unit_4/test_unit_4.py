class TestResolveCommandSubstitution:
    """Tests for resolve_command placeholder substitution."""

    def test_substitutes_run_prefix(self):
        """Substitutes {run_prefix} placeholder."""
        result = resolve_command(
            template="{run_prefix} pytest",
            env_name="svp-test",
            run_prefix="conda run -n svp-test",
        )
        assert "conda run -n svp-test" in result
        assert "{run_prefix}" not in result

    def test_substitutes_env_name(self):
        """Substitutes {env_name} placeholder."""
        result = resolve_command(
            template="conda run -n {env_name} pytest",
            env_name="svp-myproject",
            run_prefix="",
        )
        assert "svp-myproject" in result
        assert "{env_name}" not in result

    def test_substitutes_python_version(self):
        """Substitutes {python_version} placeholder."""
        result = resolve_command(
            template="python{python_version} -m pytest",
            env_name="svp-test",
            run_prefix="",
            python_version="3.11",
        )
        assert "python3.11" in result
        assert "{python_version}" not in result

    def test_substitutes_flags(self):
        """Substitutes {flags} placeholder."""
        result = resolve_command(
            template="pytest {flags} tests/",
            env_name="svp-test",
            run_prefix="",
            flags="--verbose --tb=short",
        )
        assert "--verbose --tb=short" in result
        assert "{flags}" not in result

    def test_substitutes_target(self):
        """Substitutes {target} placeholder."""
        result = resolve_command(
            template="ruff check {target}",
            env_name="svp-test",
            run_prefix="",
            target="src/unit_4/",
        )
        assert "src/unit_4/" in result
        assert "{target}" not in result

    def test_substitutes_all_placeholders_together(self):
        """All placeholders resolved in a single template."""
        result = resolve_command(
            template="{run_prefix} python{python_version} -m pytest {flags} {target}",
            env_name="svp-proj",
            run_prefix="conda run -n svp-proj",
            target="tests/",
            python_version="3.11",
            flags="-v",
        )
        assert "conda run -n svp-proj" in result
        assert "python3.11" in result
        assert "-v" in result
        assert "tests/" in result
        assert "{run_prefix}" not in result
        assert "{python_version}" not in result
        assert "{flags}" not in result
        assert "{target}" not in result

    def test_env_name_in_template(self):
        """env_name substitution works within larger template."""
        result = resolve_command(
            template="conda activate {env_name} && pytest",
            env_name="svp-demo",
            run_prefix="",
        )
        assert "svp-demo" in result
        assert "{env_name}" not in result


