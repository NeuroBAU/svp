class TestResolveCommandSubstitutionOrder:
    """Tests for single-pass substitution order: run_prefix first, then env_name, python_version, flags, target."""

    def test_run_prefix_substituted_first(self):
        """run_prefix is substituted before env_name, so run_prefix containing {env_name} literal is not re-expanded."""
        # If run_prefix contains the literal string "{env_name}", that should remain
        # because run_prefix is substituted first and env_name substitution happens after.
        # However, the contract says single-pass: run_prefix first, then env_name.
        # So {env_name} in the template (not in run_prefix value) gets substituted.
        result = resolve_command(
            template="{run_prefix} {env_name}",
            env_name="myenv",
            run_prefix="prefix",
        )
        assert result == "prefix myenv"

    def test_empty_run_prefix_does_not_leave_artifacts(self):
        """Empty run_prefix + whitespace collapse produces clean output."""
        result = resolve_command(
            template="{run_prefix} pytest {target}",
            env_name="svp-test",
            run_prefix="",
            target="tests/",
        )
        # After collapse: no double spaces, no leading/trailing whitespace
        assert "  " not in result
        assert result == result.strip()
        assert "pytest" in result


