class TestForwardReferenceGuard:
    """Detailed tests for the forward-reference guard in generate_upstream_stubs."""

    def _make_minimal_blueprint(self, tmp_path, unit_numbers):
        """Create a minimal blueprint with Python Tier 2 blocks."""
        sections = []
        for n in unit_numbers:
            sections.append(
                textwrap.dedent(f"""\
                ## Unit {n}: Unit{n}

                ### Tier 2 -- Signatures

                ```python
                def unit_{n}_func() -> None:
                    ...
                ```

                ### Tier 3 -- Behavioral Contracts

                No deps.
            """)
            )
        content = "\n".join(sections)
        bp_dir = tmp_path / "blueprint"
        bp_dir.mkdir(exist_ok=True)
        (bp_dir / "blueprint_contracts.md").write_text(content)
        (bp_dir / "blueprint_prose.md").write_text("")
        return bp_dir

    def test_forward_ref_unit_number_plus_one(self, tmp_path):
        """Dependency exactly one above unit_number is a forward reference."""
        bp_dir = self._make_minimal_blueprint(tmp_path, [5, 6])
        output_dir = tmp_path / "out"
        output_dir.mkdir()
        with pytest.raises(ValueError):
            generate_upstream_stubs(
                bp_dir,
                unit_number=5,
                upstream_units=[6],
                output_dir=output_dir,
                language="python",
            )

    def test_backward_ref_unit_number_minus_one_succeeds(self, tmp_path):
        """Dependency exactly one below unit_number succeeds."""
        bp_dir = self._make_minimal_blueprint(tmp_path, [4, 5])
        output_dir = tmp_path / "out"
        output_dir.mkdir()
        generate_upstream_stubs(
            bp_dir,
            unit_number=5,
            upstream_units=[4],
            output_dir=output_dir,
            language="python",
        )
        assert len(list(output_dir.iterdir())) >= 1

    def test_forward_ref_large_gap(self, tmp_path):
        """Dependency far above unit_number is still caught as a forward reference."""
        bp_dir = self._make_minimal_blueprint(tmp_path, [1, 100])
        output_dir = tmp_path / "out"
        output_dir.mkdir()
        with pytest.raises(ValueError):
            generate_upstream_stubs(
                bp_dir,
                unit_number=1,
                upstream_units=[100],
                output_dir=output_dir,
                language="python",
            )

    def test_multiple_valid_backward_refs(self, tmp_path):
        """Multiple valid backward references all succeed."""
        bp_dir = self._make_minimal_blueprint(tmp_path, [1, 2, 3, 4, 10])
        output_dir = tmp_path / "out"
        output_dir.mkdir()
        generate_upstream_stubs(
            bp_dir,
            unit_number=10,
            upstream_units=[1, 2, 3, 4],
            output_dir=output_dir,
            language="python",
        )
        assert len(list(output_dir.iterdir())) >= 4

    def test_single_forward_ref_among_valid_raises(self, tmp_path):
        """A single forward reference among otherwise valid deps raises ValueError."""
        bp_dir = self._make_minimal_blueprint(tmp_path, [1, 2, 3, 15, 10])
        output_dir = tmp_path / "out"
        output_dir.mkdir()
        with pytest.raises(ValueError):
            generate_upstream_stubs(
                bp_dir,
                unit_number=10,
                upstream_units=[1, 2, 3, 15],
                output_dir=output_dir,
                language="python",
            )
