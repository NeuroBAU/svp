"""Unit 10: Stub Generator Dispatch -- complete test suite.

Synthetic data assumptions:
- STUB_GENERATORS is a dict mapping language dispatch keys (str) to callables
  that accept (parsed_signatures: Any, language_config: Dict[str, Any]) and
  return a stub string.
- Key "python": generates Python stubs. Every function body contains
  `raise NotImplementedError()`. Every class body has methods raising
  NotImplementedError() and class-level attributes set to None. Module-level
  assert statements are stripped. The stub sentinel is prepended as the first
  non-import statement.
- Key "r": generates R stubs. Function bodies contain `stop("Not implemented")`.
  The R sentinel is prepended as a comment.
- Key "stan_template": generates a minimal Stan model template with a sentinel
  comment. Receives None as parsed_signatures (bypasses signature parsing).
- Keys "plugin_markdown", "plugin_bash", "plugin_json": generate minimal valid
  stubs for plugin artifact types. Receive None as parsed_signatures.
- generate_stub dispatches via language_config["stub_generator_key"].
  For plugin and component dispatch keys, parsed_signatures is bypassed (None
  passed to generator). For full languages (Python, R), the language-specific
  sentinel from LANGUAGE_REGISTRY[language]["stub_sentinel"] is prepended
  verbatim.
- Sentinel injection invariant (Bug S3-2, S3-4): the sentinel string MUST be
  the exact value from LANGUAGE_REGISTRY, injected verbatim. No reformatting.
- generate_upstream_stubs: for each upstream unit, extracts Tier 2, parses
  signatures, generates stub, writes to output_dir. Forward-reference guard:
  every dependency must have a lower unit number than unit_number; raises
  ValueError on forward reference.
- main CLI: --blueprint (path), --unit (int), --output-dir (path),
  --upstream (comma-separated ints or empty string). Generates stub for current
  unit and upstream stubs for dependencies. Exit 0 on success, 1 on failure.
- Python sentinel: '__SVP_STUB__ = True  # DO NOT DELIVER -- stub file generated by SVP'
- R sentinel: '# __SVP_STUB__ <- TRUE  # DO NOT DELIVER -- stub file generated by SVP'
- Valid Python Tier 2 source: "def foo(x: int) -> str: ..." produces a stub
  with `def foo(x: int) -> str:` and body `raise NotImplementedError()`.
- Valid R Tier 2 source: "my_func <- function(x, y) { x + y }" produces a stub
  with `stop("Not implemented")` in the body.
- A UnitDefinition from Unit 8 has fields: number, name, tier1, tier2, tier3,
  dependencies, languages, machinery.
"""

