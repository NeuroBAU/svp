class TestPythonStubGenerator:
    """Tests for the Python stub generator via STUB_GENERATORS['python']."""

    def test_python_generator_returns_string(self):
        """Python stub generator returns a string."""
        parsed = make_python_ast(PYTHON_SIMPLE_FUNC)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        assert isinstance(result, str)

    def test_python_function_body_has_raise_not_implemented(self):
        """Every function body in a Python stub must contain raise NotImplementedError()."""
        parsed = make_python_ast(PYTHON_SIMPLE_FUNC)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        assert "raise NotImplementedError()" in result

    def test_python_class_methods_raise_not_implemented(self):
        """Class methods in a Python stub must raise NotImplementedError()."""
        parsed = make_python_ast(PYTHON_MULTI_CLASS)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        # There should be at least two raises for the two methods
        assert result.count("raise NotImplementedError()") >= 2

    def test_python_class_attributes_set_to_none(self):
        """Class-level attributes in a Python stub must be set to None."""
        parsed = make_python_ast(PYTHON_MULTI_CLASS)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        # Alpha.x and Beta.y should be set to None
        assert "= None" in result

    def test_python_module_level_asserts_stripped(self):
        """Module-level assert statements must be stripped from Python stubs."""
        parsed = make_python_ast(PYTHON_WITH_ASSERT)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        # No assert statements should survive in the stub
        lines = result.split("\n")
        assert_lines = [line for line in lines if line.strip().startswith("assert ")]
        assert len(assert_lines) == 0, f"Found assert lines: {assert_lines}"

    def test_python_sentinel_prepended(self):
        """The Python stub sentinel must be prepended as the first non-import statement."""
        parsed = make_python_ast(PYTHON_TIER2_SOURCE)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        assert PYTHON_SENTINEL in result

    def test_python_sentinel_is_first_non_import_line(self):
        """The sentinel must appear before any non-import code in the stub."""
        parsed = make_python_ast(PYTHON_TIER2_SOURCE)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        lines = result.split("\n")
        sentinel_index = None
        first_non_import_index = None
        for i, line in enumerate(lines):
            stripped = line.strip()
            if not stripped or stripped.startswith("#"):
                continue
            if stripped.startswith(("import ", "from ")):
                continue
            if PYTHON_SENTINEL in line:
                sentinel_index = i
                break
            if first_non_import_index is None:
                first_non_import_index = i
                break
        # Sentinel should appear at or before first non-import line
        if sentinel_index is not None and first_non_import_index is not None:
            assert sentinel_index <= first_non_import_index
        elif sentinel_index is not None:
            # Sentinel found, no other non-import code before it -- correct
            pass
        else:
            pytest.fail("Sentinel not found in stub output")

    def test_python_stub_preserves_function_signature(self):
        """Function signatures (name, params, return type) must be preserved in the stub."""
        parsed = make_python_ast(PYTHON_SIMPLE_FUNC)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        assert "def foo" in result
        assert "x" in result

    def test_python_stub_preserves_class_name(self):
        """Class names must be preserved in the generated Python stub."""
        parsed = make_python_ast(PYTHON_MULTI_CLASS)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        assert "class Alpha" in result
        assert "class Beta" in result

    def test_python_stub_preserves_imports(self):
        """Import statements from the source must be preserved in the stub."""
        parsed = make_python_ast(PYTHON_TIER2_SOURCE)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        assert "from typing import" in result or "import typing" in result
        assert "from pathlib import Path" in result or "import pathlib" in result

    def test_python_stub_multi_function_all_raise(self):
        """A stub with multiple functions must have raise NotImplementedError() in each."""
        source = textwrap.dedent("""\
            def alpha(x: int) -> int:
                ...

            def beta(y: str) -> str:
                ...

            def gamma() -> None:
                ...
        """)
        parsed = make_python_ast(source)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        assert result.count("raise NotImplementedError()") >= 3

    def test_python_stub_is_valid_python(self):
        """The generated Python stub must be syntactically valid Python."""
        parsed = make_python_ast(PYTHON_TIER2_SOURCE)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        # Should parse without SyntaxError
        ast.parse(result)

    def test_python_stub_complex_class_with_methods_and_attributes(self):
        """A class with both attributes and methods produces correct stub structure."""
        source = textwrap.dedent("""\
            class DataProcessor:
                name: str
                value: int

                def run(self, data) -> None:
                    ...

                def reset(self) -> None:
                    ...
        """)
        parsed = make_python_ast(source)
        result = STUB_GENERATORS["python"](parsed, PYTHON_LANGUAGE_CONFIG)
        assert "class DataProcessor" in result
        assert "def run" in result
        assert "def reset" in result
        assert "raise NotImplementedError()" in result

    def test_python_stub_empty_source_still_has_sentinel(self):
        """Even with an empty source, the generated Python stub contains the sentinel."""
