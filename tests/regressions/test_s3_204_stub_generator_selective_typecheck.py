def test_k2_pure_annotation_use_stays_type_checking():
    """Upstream symbols referenced ONLY in annotations stay under
    TYPE_CHECKING (preserves S3-47 import-path-resolution-variability
    isolation for the type-only case)."""
    source = textwrap.dedent("""\
        from __future__ import annotations
        from wgcna.types import LoadResult

        def load(path: str) -> LoadResult:
            ...
    """)
    parsed = _make_ast(source)
    stub = _generate_python_stub(parsed, PYTHON_LANGUAGE_CONFIG)
    # Annotation-only upstream MUST be inside the TYPE_CHECKING guard.
    assert "if TYPE_CHECKING:" in stub
    guard_idx = stub.index("if TYPE_CHECKING:")
    after_guard = stub[guard_idx:]
    assert "from wgcna.types import LoadResult" in after_guard, (
        "annotation-only upstream import MUST emit inside TYPE_CHECKING"
    )
    # Annotations are stringified by `from __future__ annotations`, so
    # the stub still execs cleanly without the upstream module loadable.
    snapshot = sys.modules.pop("wgcna.types", None)
    try:
        sys.modules.pop("wgcna.types", None)
        # Don't install wgcna.types: stub must exec without it.
        ns: dict = {}
        exec(compile(stub, "<k2_anno_only>", "exec"), ns)
        assert "load" in ns
    finally:
        if snapshot is not None:
            sys.modules["wgcna.types"] = snapshot


def test_k2_mixed_runtime_and_annotation_uses_emit_at_runtime():
    """When a single import line binds both a runtime-used name and an
    annotation-only name, the entire line emits at runtime (conservative
    classification; safe for runtime use, retains type-resolution
    availability). C-10-K2a final clause."""
    source = textwrap.dedent("""\
        from __future__ import annotations
        from wgcna.config import WGCNAError, RAW_DEFAULT

        # WGCNAError is used as a class base (runtime).
        # RAW_DEFAULT is used only as a default arg value (runtime).
        # Both are runtime; the line emits at runtime.

        class CustomError(WGCNAError):
            def __init__(self, msg: str = RAW_DEFAULT) -> None:
                ...
    """)
    parsed = _make_ast(source)
    stub = _generate_python_stub(parsed, PYTHON_LANGUAGE_CONFIG)
    # The shared import line must emit OUTSIDE TYPE_CHECKING.
    assert "from wgcna.config import WGCNAError, RAW_DEFAULT" in stub
    if "if TYPE_CHECKING:" in stub:
        runtime_pos = stub.index("from wgcna.config import WGCNAError, RAW_DEFAULT")
        guard_pos = stub.index("if TYPE_CHECKING:")
        assert runtime_pos < guard_pos
    ns = _exec_stub(stub)
    assert "CustomError" in ns
