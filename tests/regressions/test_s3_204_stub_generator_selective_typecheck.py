"""Cycle K-2 (S3-204) -- stub generator selective TYPE_CHECKING wrap.

S3-47 originally wrapped every non-stdlib (upstream) import in
``if TYPE_CHECKING:`` on the premise that "stubs never execute upstream
code at runtime, so upstream imports are only needed for type annotations."
The premise was incomplete: function/method bodies are
``raise NotImplementedError()``, but Python evaluates **class base
classes, decorators, and default argument values at module-load time**.
A Tier-2 signature like ``class DataLoadError(WGCNAError): ...`` evaluates
``WGCNAError`` when the stub module is imported -- so wrapping
``from wgcna.config import WGCNAError`` under ``TYPE_CHECKING`` (False at
runtime) raises ``NameError`` at exec-time, even though the stub
``compile()``s cleanly.

The fix: ``_generate_python_stub`` walks the parsed Tier-2 body to
identify upstream-imported names referenced at module-load time
(``ClassDef.bases``/``keywords``, ``decorator_list``,
``arguments.defaults``/``kw_defaults``, ``Assign.value``/``AnnAssign.value``).
Imports providing those names emit OUTSIDE the ``TYPE_CHECKING`` guard;
type-only-use imports stay INSIDE.

These tests use ``exec()`` -- not just ``compile()`` -- because module-load
NameErrors are runtime errors that ``compile()`` does not detect (Pattern
P89 test discipline; C-10-K2b acceptance mandate).

Pattern reference: P89 (TYPE_CHECKING wraps must be selective re
module-load-time references).
"""

from __future__ import annotations

import ast
import sys
import textwrap
import types

from stub_generator import _generate_python_stub


PYTHON_SENTINEL = "_SVP_SENTINEL_TOKEN_ = True  # DO NOT DELIVER -- stub file generated by SVP"

PYTHON_LANGUAGE_CONFIG = {
    "id": "python",
    "stub_generator_key": "python",
    "stub_sentinel": PYTHON_SENTINEL,
    "is_component_only": False,
}


def _make_ast(source: str) -> ast.Module:
    return ast.parse(source)


def _install_fake_upstream_modules() -> dict:
    """Install synthetic upstream modules so the stub can `exec` cleanly.

    Returns a snapshot of which modules were installed, so tests can clean up.
    """
    installed: dict = {}
    wgcna_pkg = types.ModuleType("wgcna")
    wgcna_config = types.ModuleType("wgcna.config")

    class WGCNAError(Exception):
        pass

    def log_event(cls):
        return cls

    wgcna_config.WGCNAError = WGCNAError
    wgcna_config.log_event = log_event
    wgcna_config.RAW_DEFAULT = "raw_default"
    wgcna_types = types.ModuleType("wgcna.types")
    wgcna_types.LoadResult = list
    wgcna_pkg.config = wgcna_config
    wgcna_pkg.types = wgcna_types
    for name, mod in [
        ("wgcna", wgcna_pkg),
        ("wgcna.config", wgcna_config),
        ("wgcna.types", wgcna_types),
    ]:
        installed[name] = sys.modules.get(name)
        sys.modules[name] = mod
    return installed


def _restore_modules(snapshot: dict) -> None:
    for name, prior in snapshot.items():
        if prior is None:
            sys.modules.pop(name, None)
        else:
            sys.modules[name] = prior


def _exec_stub(stub_source: str) -> dict:
    """Compile + exec the stub. Returns the module namespace."""
    snapshot = _install_fake_upstream_modules()
    try:
        ns: dict = {}
        exec(compile(stub_source, "<k2_stub>", "exec"), ns)
        return ns
    finally:
        _restore_modules(snapshot)


