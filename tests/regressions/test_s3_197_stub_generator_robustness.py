def test_h7_full_stub_pipeline_with_dict_pseudocode_and_no_upstream_imports():
    """End-to-end: blueprint Tier-2 source contains {"foo": "bar", ...} AND has
    no upstream imports. R1 #9 strips the bare ellipsis; R1 #7 emits the
    from __future__ preamble first; the resulting stub exec()s cleanly."""
    source = textwrap.dedent("""\
        def foo() -> dict:
            return {"key": "val", ...}
    """)
    # R1 #9: parse without SyntaxError despite the bare ellipsis
    parsed = _parse_python_signatures(source, {})
    assert isinstance(parsed, ast.Module)
    # R1 #7: stub has from __future__ as line 1, no upstream imports needed
    stub = _generate_python_stub(parsed, PYTHON_LANGUAGE_CONFIG)
    assert _first_non_blank_line(stub) == FUTURE_LINE
    # Sentinel comes after from __future__
    future_idx = stub.index(FUTURE_LINE)
    sentinel_idx = stub.index(PYTHON_SENTINEL)
    assert sentinel_idx > future_idx
    # Stub exec()s without NameError
    namespace: dict = {}
    exec(stub, namespace)  # noqa: S102 -- intentional exec for stub validation
    assert "foo" in namespace
