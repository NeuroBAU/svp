def test_h7_signature_parser_handles_bare_ellipsis_in_dict_literal():
    """The R1 #9 bug: source containing {"key": "val", ...} parses without
    SyntaxError after the regex preprocess strips the bare ', ...' pattern."""
    source = textwrap.dedent("""\
        def foo() -> dict:
            return {"key": "val", ...}
    """)
    # Should NOT raise SyntaxError
    result = _parse_python_signatures(source, {})
    assert result is not None
    assert isinstance(result, ast.Module)


def test_h7_signature_parser_preserves_valid_python_unchanged():
    """Regression guard: valid Python with no ', ...' patterns produces an AST
    structurally identical to plain ast.parse(source)."""
    source = textwrap.dedent("""\
        def foo() -> dict:
            return {"key": "val"}
    """)
    expected = ast.parse(source)
    result = _parse_python_signatures(source, {})
    assert ast.dump(result) == ast.dump(expected)


def test_h7_signature_parser_handles_multiple_ellipsis_patterns_in_one_block():
    """Multiple {"a": 1, ...} patterns in one source string all stripped, so
    the whole block parses cleanly."""
    source = textwrap.dedent("""\
        def foo() -> dict:
            return {"a": 1, ...}

        def bar() -> dict:
            return {"b": 2, ...}
    """)
    # Should NOT raise SyntaxError
    result = _parse_python_signatures(source, {})
    assert result is not None
    assert isinstance(result, ast.Module)
    # Both functions must be present after parsing
    func_names = [
        n.name for n in ast.walk(result) if isinstance(n, ast.FunctionDef)
    ]
    assert "foo" in func_names
    assert "bar" in func_names


