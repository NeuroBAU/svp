    def test_many_units_scaffolded_correctly(
        self,
        project_root,
        blueprint_dir,
        python_profile,
        python_toolchain,
        python_language_registry,
    ):
        """A large blueprint with many units creates all directories."""
        lines = []
        for i in range(1, 16):
            deps = "None" if i == 1 else f"Unit {i - 1}"
            lines.append(f"## Unit {i}: Unit {i}\n")
            lines.append("\n### Tier 2 -- Signatures\n\n```python\nimport os\n```\n")
            lines.append(
                f"\n### Tier 3 -- Behavioral Contracts\n\n**Dependencies:** {deps}.\n\n---\n\n"
            )
        (blueprint_dir / "blueprint_contracts.md").write_text("\n".join(lines))
        state_file = project_root / ".svp" / "pipeline_state.json"
        state_file.write_text(json.dumps({"stage": "setup"}))
        run_infrastructure_setup(
            project_root,
            python_profile,
            python_toolchain,
            python_language_registry,
            blueprint_dir,
        )
        for i in range(1, 16):
            assert (project_root / "src" / f"unit_{i}").is_dir()
            assert (project_root / "tests" / f"unit_{i}").is_dir()
        if state_file.exists():
            state = json.loads(state_file.read_text())
            if "total_units" in state:
                assert state["total_units"] == 15

